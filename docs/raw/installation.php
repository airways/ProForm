<h1>Installation</h1>

<p>Installing <dfn>ProForm</dfn> is similar to installing any third-party Module into ExpressionEngine.</p>

<h2>Installation Steps</h2>

<p>Follow these steps to install <dfn>ProForm</dfn>:</p>

<ol>
    <li>Download the ZIP file from the location where you purchased it.</li>
    <li>Extract the ZIP file.</li>
    <li>There are four folders within the extracted folder from the ZIP file. Copy each one to the matching location within your ExpressionEngine installation:
        <ul>
            <li><strong>&lt;extracted_folder&gt;</strong>/system/expressionengine/third_party/proform &rarr;
                <strong>&lt;doc_root&gt;</strong>/system/expressionengine/third_party/proform </li>
            <li><strong>&lt;extracted_folder&gt;</strong>/system/expressionengine/third_party/prolib &rarr;
                <strong>&lt;doc_root&gt;</strong>/system/expressionengine/third_party/prolib</li>
            <li><strong>&lt;extracted_folder&gt;</strong>/themes/third_party/proform &rarr;
                <strong>&lt;doc_root&gt;</strong>/themes/third_party/proform</li>
            <li><strong>&lt;extracted_folder&gt;</strong>/themes/third_party/prolib &rarr;
                <strong>&lt;doc_root&gt;</strong>/themes/third_party/prolib</li>
        </ul>
    </li>
    <li>Visit your control panel, usually located at:<br/>
        <strong>http://example.com/system/</strong>
    </li>
    <li>Click on <strong>Add-ons</strong> > <strong>Modules</strong> in the main menu.</li>
    <li>Within the list of Modules you will find an entry for <dfn>ProForm</dfn>, click on the <strong>Install</strong> link next to this entry.</li>
    <li>You will be taken to a <strong>Package Settings</strong> screen, listing entries for ProForm's components. Make sure that all components have <strong>Install</strong> selected.</li>
    <li>Click <strong>Submit</strong> to finish installation.</li>
    <li>After the module is installed, you can find a link to it's main page in the list at <strong>Add-ons</strong> > <strong>Modules</strong>.</li>
    <li>On the main <dfn>ProForm</dfn> page, you may want to click the <strong>+ Add</strong> link in the menu bar. This will add a main menu item for <dfn>ProForm</dfn> so you can get it to more easily.</li>
</ol>

<p>You may now use the control panel page for <dfn>ProForm</dfn> to being <a href="http://localhost/~iraway/ee231/system/expressionengine/third_party/proform/docs/html/cp/forms.html">Managing Forms</a>.</p>

